// push
// push to last Element
let array = [ 1,3,25,6]
array.push(90,34)
console.log(array);


// unshift
// push to first Element
array.push(23)
console.log(array);


// pop
// remove to last Element
array.pop()
console.log(array);


// shift
// remove to first Element
array.shift()
console.log(array);

